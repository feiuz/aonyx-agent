/// Eye socket centres [(left), (right)] per pose (same index as
/// `OTTER_POSES`), in art-grid cells — used to close the eyes on a blink.
pub const OTTER_EYES: [[(u16, u16); 2]; 9] = [
    [(16, 7), (34, 7)],
    [(16, 8), (33, 8)],
    [(17, 8), (33, 8)],
    [(15, 8), (33, 7)],
    [(16, 8), (33, 8)],
    [(16, 7), (34, 8)],
    [(16, 8), (32, 8)],
    [(16, 8), (33, 8)],
    [(15, 7), (33, 7)],
];

/// Map a cursor direction (dx, dy each in -1..=1) to an [`OTTER_POSES`] index.
pub fn pose_index(dx: i8, dy: i8) -> usize {
    let r = (dy.clamp(-1, 1) + 1) as usize;
    let c = (dx.clamp(-1, 1) + 1) as usize;
    r * 3 + c
}

/// Direction from the logo centre to the mouse as (dx, dy) in -1..=1,
/// with a deadzone (cells) that keeps the head centred near the middle.
pub fn direction(mouse: (u16, u16), center: (u16, u16), dz: u16) -> (i8, i8) {
    let sgn = |a: u16, b: u16| -> i8 {
        if a > b.saturating_add(dz) { 1 } else if b > a.saturating_add(dz) { -1 } else { 0 }
    };
    (sgn(mouse.0, center.0), sgn(mouse.1, center.1))
}
